//В одномерном массиве, вводимом с клавиатуры и состоящем из N (1<=N<=1000) вещественных элементов,
//вычислить количество отрицательных элементов массива.
#include <iostream>

using namespace std;

main()
{
    setlocale (0,"");
    cout<<"Введите количество цифр: ";
    int n, S=0, x[1000];
    cin>>n;
    for (int i=0;i<n;i++)
    {
        cout<<"Введите число: ";
        cin>>x[i];
        if(x[i]<0) S++;
    }
    cout<<S;
}
